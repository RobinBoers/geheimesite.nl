                <section>
                    <h1>Framboy</h1>
                    <p>
                        A Framboy is a supercharged Gameboy. It cannot only play Gameboy games, it can also play retro games up to the Nintendo 64 and all games a modern computer can play via Steam Link. Apart from that it also plays Spotify. You think that's cool? Wait until you see this: it can make calls.
                    </p>

                    <h2>Features</h2>

                    <ul>
                        <li>Wi-Fi and Bluetooth built-in</li>
                        <li>Online multiplayer</li>
                        <li>Listen to music</li>
                        <li>Play modern games</li>
                        <li>Make calls <em>(coming soon)</em></li>
                    </ul>

                    <h3>Online multiplayer</h3>

                    <p>
                        The Framboy can play online multiplayer using RetroArch <a href="https://retropie.org.uk/docs/Netplay/">Netplay</a>. That means you can play games with your friends while you're both at home. 
                    </p>

                    <h3>Spotify & Music</h3>

                    <p>
                        The Framboy plays music via Spotify. This means it allows you to listen to your favorite music while playing games with your friends. But there's more: the Framboy <a href="https://github.com/abba23/spotify-adblock">blocks ads by default</a>, and you can also download music, for listening without Wi-Fi.
                    </p>

                    <h3>Modern games</h3>

                    <p>
                        <a href="https://store.steampowered.com/app/353380/Steam_Link/">Steam Link</a> allows you to play every game you can run on a modern computer on the Framboy. It links your PC to the Framboy. The games run via the PC, but you can play always everywhere. Very nice! 
                    </p>

                    <hr class="my-7 border-none">

                    <h2>About the project</h2>

                    <p>
                        I have had the idea for this project since around 2019. When I first saw alternative OSes like Linux and Android I wondered if I could turn an old laptop I had lying around into an Xbox. Obviously, I failed. But I did discover the wonderful world of retro gaming. 
                    </p>
                    <p>
                        Ever since then I've been thinking about building a retro gaming handheld. My first attempt was in 2020 when I built the <a href="https://blog.geheimesite.nl/2020/08/de-frambo-diy-raspberry-pi-spelcomputer.html">Frambo</a>.
                    </p>

                    <p>
                        In 2021 I got really annoyed by smartphone brands like Apple and Samsung. Specifically the durability and non-repairability of the devices. I decided I wanted to <a href="https://blog.geheimesite.nl/2021/01/ik-wil-zelf-een-telefoon-maken.html">try to make my own phone</a>.
                    </p>

                    <p>
                        But when I was searching for components for the phone I realized I rather have a physical keyboard and gaming controls. I always loved the Gameboy, so I ordered a Gameboy case. I haven't finished the project yet. I'm still waiting for components (specifically the screen and the keyboard) to arrive. I decided I first wanted to create the gaming/computer aspect of the project, and then later expand it to add the ability to make phone calls.
                    </p>

                    <hr class="my-7 border-none">

                    <h2>How to build a Framboy</h2>

                    <p>The Framboy is my newest hardware project. You can build one yourself! These are the components required:</p>

                    <ul>
                        <li>PiSugar (Battery)</li>
                        <li>Raspberry Pi Zero W or Raspberry Pi Zero 2</li>
                        <li>Gameboy case</li>
                        <li>2.8-inch screen</li>
                        <li>Mini Bluetooth or USB keyboard</li>
                    </ul>

                    <p>
                        When I've finished the project I will post a full building guide and video with more info.
                    </p>

