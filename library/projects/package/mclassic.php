                    <h1>Project: js0.23-improved</h1>
                    <p>
                        js0.23-improved is een verbeterde versie van de Minecraft Classic remake uit 2019.
                        
                    </p>
                    <p>     
                        Minecraft Classic is de 2e grote versie van Minecraft en de eerste versie die publiek beschikbaar was. Minecraft Classic is ook het moment dat multiplayer en survival-mode werden geïntroduceerd.
                    </p>
                    <p>
                        Toen Minecraft in 2019 tien jaar oud werd heeft Mojang een remake van <a href="https://minecraft.fandom.com/wiki/Java_Edition_Classic_0.0.23a_01">Minecraft 0.023a_01</a> geschreven in Javascript online gezet op <a href="https://classic.minecraft.net">classic.minecraft.net</a>.
                    </p>
                    <p>
                        Omdat ik onder een steen leef kwam ik daar pas in 2021 achter. Maar toen ik er achter kwam ben ik meteen begonnen met het modden van de game (die ook wel js0.23 wordt genoemd). Ik heb onder andere een menuscherm, verbeterde muiscontrols, veborgen credits scherm, seeds, custom commands, vliegmodus en wat andere dingen toegevoegd.
                    </p>

                    <p>
                        Je kan een testversie zo in je browser spelen.<br>
                        <a href="https://code.geheimesite.nl/beta/mclassic.js/">Speel de demo</a>
                    </p>

                    <p>
                        Ik heb er ook een Electron app van gemaakt zodat je het offline, direct op je computer kan spelen.<br>
                        <a href="https://github.com/RobinBoers/js0.23-improved">Je kan de code op GitHub vinden</a>
                    </p>
